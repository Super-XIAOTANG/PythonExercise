# 一个简单的字符串格式化方法，使用%作为占位符，简画输出结构。类似于c语言的printf
# %s 字符串 %d 数字 %c单个字符 %f浮点实数
# 这种输出等效于.format(),前面用{}代表留空
print("我今年%s岁，我几年前%s岁" % (a, b))
print("我今年{}岁，我几年前{}岁".format(a, b))
