# 变量与数据类型
# 变量可以理解为是一段有名字的连续存储空间,用于存储数据。Python不需要声明变量，直接赋值即可
# 变量必须以字母或下划线_开头，可以包含数字、字母、下划线_

# 低级类型
"""
    数字 num：包含了 int、long（Python3取消）、float、complex、bool
    字符串 str
"""
